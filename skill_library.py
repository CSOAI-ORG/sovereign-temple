    lib.ingest(
        skill_hash="abc123",
        task_type="council_proposal",
        title="Haulage Contract Review",
        description="Standard workflow for reviewing and voting on haulage contracts",
        yaml_body="task_type: council_proposal\nsteps:\n  - Review terms\n  - Check compliance\n  - BFT vote",
        care_score=0.92,
    )
    lib.validate_skill("abc123")
    print("Search 'haulage':", lib.search("haulage"))
    print("Stats:", lib.stats())
